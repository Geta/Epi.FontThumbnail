namespace Geta.Epi.FontThumbnail
{
    public enum Rotations
    {
        /// <summary>
        /// Flips the icon vertically
        /// </summary>
        FlipVertical = -2,
        /// <summary>
        /// Flips the icon horizontally
        /// </summary>
        FlipHorizontal = -1,
        /// <summary>
        /// No rotation applied
        /// </summary>
        None = 0,
        /// <summary>
        /// Rotates the icon 90 degrees
        /// </summary>
        Rotate90 = 90,
        /// <summary>
        /// Rotates the icon 180 degrees
        /// </summary>
        Rotate180 = 180,
        /// <summary>
        /// Rotates the icon 270 degrees
        /// </summary>
        Rotate270 = 270
    }
}
